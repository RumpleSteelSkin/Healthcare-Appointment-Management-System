CREATE PROCEDURE sp_MigratePatientsToAspNetUsers
AS
BEGIN
    SET NOCOUNT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        -- Patients tablosundan verileri çekip, AspNetUsers tablosuna ekleme
        INSERT INTO AspNetUsers
        (
            Id,
            FirstName,
            LastName,
            UserName,
            NormalizedUserName,
            Email,
            NormalizedEmail,
            EmailConfirmed,
            PasswordHash,
            SecurityStamp,
            ConcurrencyStamp,
            PhoneNumber,
            PhoneNumberConfirmed,
            TwoFactorEnabled,
            LockoutEnd,
            LockoutEnabled,
            AccessFailedCount
        )
        SELECT 
            p.Id,
            p.FirstName,
            p.LastName,
            LOWER(p.FirstName + '.' + p.LastName) AS UserName,
            UPPER(p.FirstName + '.' + p.LastName) AS NormalizedUserName,
            LOWER(p.FirstName + '.' + p.LastName + '@example.com') AS Email,
            UPPER(p.FirstName + '.' + p.LastName + '@EXAMPLE.COM') AS NormalizedEmail,
            0, -- EmailConfirmed
            'AQAAAAIAAYagAAAAEIn8Em29ZlbaMlnvJt7OIR/e87Yo7o+0dUr2u5D9yRELuVV9P8s+i4pnd/T9w4LEXQ==', -- PasswordHash
            'HCLI74K7FP7RRFQKBONWNDFRRCA6JPDA', -- SecurityStamp
            '874c9a13-45c9-43f5-86cf-3e2673f5a564', -- ConcurrencyStamp
            NULL, -- PhoneNumber
            0,    -- PhoneNumberConfirmed
            0,    -- TwoFactorEnabled
            NULL, -- LockoutEnd
            1,    -- LockoutEnabled
            0     -- AccessFailedCount
        FROM Patients p;

        -- Eklenen her kullanýcý için AspNetUserRoles tablosuna rol atamasý yapma
        INSERT INTO AspNetUserRoles
        (
            UserId,
            RoleId
        )
        SELECT 
            p.Id,
            'BB6209AD-7D39-4572-2319-08DD73A0E20F'
        FROM Patients p;

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0
            ROLLBACK TRANSACTION;
        -- Hata durumunda hata bilgilerini fýrlatýyoruz
        THROW;
    END CATCH
END;

GO
